<h1 class="text-2xl font-bold">Pubcrawl</h1>

<p>
	A web app that allows door attendants at TLTH pubs to register participants. The information is
	updated in real-time, allowing others to see how many people are at each pub during the pub crawl.
</p>
<br />
<p>
	Created by the <a class="link" href="https://dsek.se/committees/cpu">CPU</a> Committee, of the
	<a class="link" href="https://dsek.se">D-guild</a>.
</p>
