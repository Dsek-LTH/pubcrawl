<h1 class="text-2xl font-bold">Pubcrawl</h1>

<p>
	Pubcrawl is a web app that allows door attendants at TLTH pubs to register participants. The information is
	updated in real-time, allowing visitors to see how many people are at each pub during the pub crawl.
</p>
<br />
<p class="mt-10 mb-4">
	Created by the <a class="link" href="https://dsek.se/committees/cpu">CPU</a> Committee, of the
	<a class="link" href="https://dsek.se">D-guild</a>.
